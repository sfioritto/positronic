#!/usr/bin/env node
// Generates src/component-sources.gen.ts — a map from each named export in
// src/components/*.tsx to the full source of the file that exports it. Used
// by the `show_component_source` tool at runtime so the generator LLM can
// inspect how any component is actually implemented.

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const COMPONENTS_DIR = join(__dirname, '..', 'src', 'components');
const OUT_FILE = join(__dirname, '..', 'src', 'component-sources.gen.ts');

function parseExports(source) {
  // Matches trailing `export { A, B, C, ... }` block — works for every
  // component file in this package.
  const match = source.match(/export\s*\{([^}]+)\}\s*;?\s*$/);
  if (!match) return [];
  return match[1]
    .split(',')
    .map((s) =>
      s
        .trim()
        .split(/\s+as\s+/)
        .pop()
    )
    .filter(Boolean);
}

const files = (await readdir(COMPONENTS_DIR))
  .filter((f) => f.endsWith('.tsx'))
  .sort();

const map = {};
const fileInfo = [];

for (const file of files) {
  const source = await readFile(join(COMPONENTS_DIR, file), 'utf8');
  const exports = parseExports(source);
  if (exports.length === 0) {
    console.warn(`gen-component-sources: no exports found in ${file}`);
    continue;
  }
  fileInfo.push({ file, exports });
  for (const name of exports) {
    if (map[name]) {
      console.warn(
        `gen-component-sources: duplicate export "${name}" — ${map[name].fileName} and ${file}`
      );
    }
    map[name] = { fileName: file, source, exports };
  }
}

// Deduplicate source strings via a shared sources table so the emitted file
// doesn't embed 28 full TSX files 1-to-5 times each.
const sourceIndex = new Map();
const sources = [];
for (const { file } of fileInfo) {
  const src = await readFile(join(COMPONENTS_DIR, file), 'utf8');
  sourceIndex.set(file, sources.length);
  sources.push({ fileName: file, source: src });
}

const entries = Object.entries(map)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([name, info]) => {
    const idx = sourceIndex.get(info.fileName);
    return `  ${JSON.stringify(name)}: { fileName: ${JSON.stringify(
      info.fileName
    )}, exports: ${JSON.stringify(info.exports)}, sourceIndex: ${idx} }`;
  })
  .join(',\n');

const sourcesLiteral = sources
  .map(
    (s) =>
      `  { fileName: ${JSON.stringify(s.fileName)}, source: ${JSON.stringify(
        s.source
      )} }`
  )
  .join(',\n');

const output = `// Generated by scripts/gen-component-sources.mjs — do not edit.
export interface ComponentSourceEntry {
  fileName: string;
  exports: string[];
  sourceIndex: number;
}

export const COMPONENT_SOURCE_FILES: ReadonlyArray<{
  fileName: string;
  source: string;
}> = [
${sourcesLiteral}
];

export const COMPONENT_SOURCE_INDEX: Record<string, ComponentSourceEntry> = {
${entries}
};

export function getComponentSource(name: string): {
  fileName: string;
  source: string;
  exports: string[];
} | null {
  const entry = COMPONENT_SOURCE_INDEX[name];
  if (!entry) return null;
  const file = COMPONENT_SOURCE_FILES[entry.sourceIndex];
  return { fileName: entry.fileName, source: file.source, exports: entry.exports };
}

export function listComponentNames(): string[] {
  return Object.keys(COMPONENT_SOURCE_INDEX);
}
`;

await writeFile(OUT_FILE, output, 'utf8');
console.log(
  `gen-component-sources: wrote ${OUT_FILE} (${
    Object.keys(map).length
  } exports across ${sources.length} files)`
);
